/**
 * ContactPersonListMsg<p/>Default message for list of ContactPerson datatypes<p/>
 *
 * @version 1.0
 * @author Raffael Bieniek, PRODYNA AG, 2011-06-07
 */
public class ContactPersonListMsg extends ServiceMessageSupport implements ServiceMessage {

    private static final long serialVersionUID = 1L;

    private static final String[] PROPERTY_CONSTRAINTS = { "m0,n;" };

    public static final String CONTACTPERSONLIST = "contactPersonList";

    private NabuccoList<ContactPerson> contactPersonList;

    /** Constructs a new ContactPersonListMsg instance. */
    public ContactPersonListMsg() {
        super();
        this.initDefaults();
    }

    /** InitDefaults. */
    private void initDefaults() {
    }

    /**
     * CreatePropertyContainer.
     *
     * @return the NabuccoPropertyContainer.
     */
    protected static NabuccoPropertyContainer createPropertyContainer() {
        Map<String, NabuccoPropertyDescriptor> propertyMap = new HashMap<String, NabuccoPropertyDescriptor>();
        propertyMap.put(CONTACTPERSONLIST, PropertyDescriptorSupport.createCollection(CONTACTPERSONLIST,
                ContactPerson.class, 0, PROPERTY_CONSTRAINTS[0], false, PropertyAssociationType.COMPOSITION));
        return new NabuccoPropertyContainer(propertyMap);
    }

    /** Init. */
    public void init() {
        this.initDefaults();
    }

    @Override
    public Set<NabuccoProperty> getProperties() {
        Set<NabuccoProperty> properties = super.getProperties();
        properties.add(super.createProperty(ContactPersonListMsg.getPropertyDescriptor(CONTACTPERSONLIST),
                this.contactPersonList));
        return properties;
    }

    @Override
    @SuppressWarnings("unchecked")
    public boolean setProperty(NabuccoProperty property) {
        if (super.setProperty(property)) {
            return true;
        }
        if ((property.getName().equals(CONTACTPERSONLIST) && (property.getType() == ContactPerson.class))) {
            this.contactPersonList = ((NabuccoList<ContactPerson>) property.getInstance());
            return true;
        }
        return false;
    }

    @Override
    public boolean equals(Object obj) {
        if ((this == obj)) {
            return true;
        }
        if ((obj == null)) {
            return false;
        }
        if ((this.getClass() != obj.getClass())) {
            return false;
        }
        if ((!super.equals(obj))) {
            return false;
        }
        final ContactPersonListMsg other = ((ContactPersonListMsg) obj);
        if ((this.contactPersonList == null)) {
            if ((other.contactPersonList != null))
                return false;
        } else if ((!this.contactPersonList.equals(other.contactPersonList)))
            return false;
        return true;
    }

    @Override
    public int hashCode() {
        final int PRIME = 31;
        int result = super.hashCode();
        result = ((PRIME * result) + ((this.contactPersonList == null) ? 0 : this.contactPersonList.hashCode()));
        return result;
    }

    @Override
    public ServiceMessage cloneObject() {
        return this;
    }

    /**
     * Missing description at method getContactPersonList.
     *
     * @return the NabuccoList<ContactPerson>.
     */
    public NabuccoList<ContactPerson> getContactPersonList() {
        if ((this.contactPersonList == null)) {
            this.contactPersonList = new NabuccoListImpl<ContactPerson>(NabuccoCollectionState.INITIALIZED);
        }
        return this.contactPersonList;
    }

    /**
     * Getter for the PropertyDescriptor.
     *
     * @param propertyName the String.
     * @return the NabuccoPropertyDescriptor.
     */
    public static NabuccoPropertyDescriptor getPropertyDescriptor(String propertyName) {
        return PropertyCache.getInstance().retrieve(ContactPersonListMsg.class).getProperty(propertyName);
    }

    /**
     * Getter for the PropertyDescriptorList.
     *
     * @return the List<NabuccoPropertyDescriptor>.
     */
    public static List<NabuccoPropertyDescriptor> getPropertyDescriptorList() {
        return PropertyCache.getInstance().retrieve(ContactPersonListMsg.class).getAllProperties();
    }
}
